package com.rainmonth.pattern.creational.builder2;

public class UserDemo {
    public static void main(String[] args) {

        User userKaren = new User.UserBuilder("Karen", "Lin").build();

        System.out.println("User's first Name: " + userKaren.getFirstName());
        System.out.println("User's last Name: " + userKaren.getLastName());
        System.out.println("User's age: " + userKaren.getAge());
        System.out.println("User's phone: " + userKaren.getPhone());
        System.out.println("User's address: " + userKaren.getAddress() + "\n");

        User userGrass = new User.UserBuilder("Grass", "Dig").age(18).phone("7533967").build();

        System.out.println("User's first Name: " + userGrass.getFirstName());
        System.out.println("User's last Name: " + userGrass.getLastName());
        System.out.println("User's age: " + userGrass.getAge());
        System.out.println("User's phone: " + userGrass.getPhone());
        System.out.println("User's address: " + userGrass.getAddress());
    }
}
